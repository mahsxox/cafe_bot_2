async def create_buttons(option):
    """
    –°–æ–∑–¥–∞—ë—Ç –∏–Ω–ª–∞–π–Ω-–∫–Ω–æ–ø–∫–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞ –æ–ø—Ü–∏–π.

    –ö–∞–∂–¥–∞—è —Å—Ç—Ä–æ–∫–∞ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã —Å–æ–¥–µ—Ä–∂–∏—Ç –æ–¥–Ω—É –∫–Ω–æ–ø–∫—É, —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é —ç–ª–µ–º–µ–Ω—Ç—É –∏–∑ —Å–ø–∏—Å–∫–∞.

    Args:
        option (list): –°–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫, –∫–∞–∂–¥–∞—è –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö —Å—Ç–∞–Ω–µ—Ç —Ç–µ–∫—Å—Ç–æ–º –∫–Ω–æ–ø–∫–∏.

    Returns:
        InlineKeyboardMarkup: –ò–Ω–ª–∞–π–Ω-–∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞, –≥–¥–µ –∫–∞–∂–¥–∞—è –∫–Ω–æ–ø–∫–∞ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
    """
    buttons = []
    for text in option:
        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Ç–µ–∫—Å—Ç –≤ callback_data, –∑–∞–º–µ–Ω—è—è –ø—Ä–æ–±–µ–ª—ã –Ω–∞ –ø–æ–¥—á—ë—Ä–∫–∏–≤–∞–Ω–∏—è
        callback_data = f'selected_{text}'.replace(' ', '_')
        # –°–æ–∑–¥–∞—ë–º –∫–Ω–æ–ø–∫—É —Å —Ç–µ–∫—Å—Ç–æ–º –∏ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–º callback_data
        button = InlineKeyboardButton(text=text, callback_data=callback_data)
        # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ
        buttons.append([button])
    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –≥–æ—Ç–æ–≤—É—é –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
    return InlineKeyboardMarkup(inline_keyboard=buttons)


